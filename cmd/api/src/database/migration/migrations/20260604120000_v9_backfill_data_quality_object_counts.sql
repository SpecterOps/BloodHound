CREATE TABLE IF NOT EXISTS data_quality_object_count_runs (
    run_id text PRIMARY KEY,
    created_at timestamp with time zone NOT NULL,
    updated_at timestamp with time zone NOT NULL
);

CREATE TABLE IF NOT EXISTS data_quality_source_object_counts (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    source_kind text NOT NULL,
    node_kind text NOT NULL,
    count bigint NOT NULL,
    run_id text NOT NULL,
    created_at timestamp with time zone,
    updated_at timestamp with time zone
);

CREATE TABLE IF NOT EXISTS data_quality_environment_object_counts (
    id integer GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    source_kind text NOT NULL,
    environment_kind text NOT NULL,
    environment_id text NOT NULL,
    node_kind text NOT NULL,
    count bigint NOT NULL,
    run_id text NOT NULL,
    created_at timestamp with time zone,
    updated_at timestamp with time zone
);

CREATE INDEX IF NOT EXISTS idx_dq_object_count_runs_created_at ON data_quality_object_count_runs USING btree (created_at);

CREATE INDEX IF NOT EXISTS idx_dq_source_obj_counts_run_id ON data_quality_source_object_counts USING btree (run_id);
CREATE INDEX IF NOT EXISTS idx_dq_source_obj_counts_source_node_kind ON data_quality_source_object_counts USING btree (source_kind, node_kind);
CREATE INDEX IF NOT EXISTS idx_dq_source_obj_counts_created_at ON data_quality_source_object_counts USING btree (created_at);
CREATE UNIQUE INDEX IF NOT EXISTS idx_dq_source_obj_counts_run_source_node_kind ON data_quality_source_object_counts USING btree (run_id, source_kind, node_kind);

CREATE INDEX IF NOT EXISTS idx_dq_env_obj_counts_run_id ON data_quality_environment_object_counts USING btree (run_id);
CREATE INDEX IF NOT EXISTS idx_dq_env_obj_counts_source_env_node_kind ON data_quality_environment_object_counts USING btree (source_kind, environment_kind, environment_id, node_kind);
CREATE INDEX IF NOT EXISTS idx_dq_env_obj_counts_source_node_kind ON data_quality_environment_object_counts USING btree (source_kind, node_kind);
CREATE INDEX IF NOT EXISTS idx_dq_env_obj_counts_created_at ON data_quality_environment_object_counts USING btree (created_at);
CREATE UNIQUE INDEX IF NOT EXISTS idx_dq_env_obj_counts_run_source_env_node_kind ON data_quality_environment_object_counts USING btree (run_id, source_kind, environment_kind, environment_id, node_kind);

