INSERT INTO data_quality_object_count_runs (run_id, created_at, updated_at)
SELECT run_id, COALESCE(MIN(created_at), NOW()), COALESCE(MAX(updated_at), NOW())
FROM (
    SELECT run_id, created_at, updated_at
    FROM data_quality_source_object_counts

    UNION ALL

    SELECT run_id, created_at, updated_at
    FROM data_quality_environment_object_counts
) AS object_count_runs
WHERE run_id <> ''
GROUP BY run_id
ON CONFLICT (run_id) DO NOTHING;

