INSERT INTO data_quality_source_object_counts (source_kind, node_kind, count, run_id, created_at, updated_at)
SELECT source_kind, node_kind, object_count, run_id, created_at, updated_at
FROM (
    SELECT 'Base' AS source_kind, node_kind, object_count::bigint, run_id, created_at, updated_at
    FROM ad_data_quality_aggregations
    CROSS JOIN LATERAL (VALUES
        ('Domain', domains),
        ('User', users),
        ('Group', groups),
        ('Computer', computers),
        ('OU', ous),
        ('Container', containers),
        ('GPO', gpos),
        ('AIACA', aiacas),
        ('RootCA', rootcas),
        ('EnterpriseCA', enterprisecas),
        ('NTAuthStore', ntauthstores),
        ('CertTemplate', certtemplates),
        ('IssuancePolicy', issuancepolicies)
    ) AS counts(node_kind, object_count)

    UNION ALL

    SELECT 'AZBase' AS source_kind, node_kind, object_count::bigint, run_id, created_at, updated_at
    FROM azure_data_quality_aggregations
    CROSS JOIN LATERAL (VALUES
        ('AZTenant', tenants),
        ('AZUser', users),
        ('AZGroup', groups),
        ('AZApp', apps),
        ('AZServicePrincipal', service_principals),
        ('AZDevice', devices),
        ('AZManagementGroup', management_groups),
        ('AZSubscription', subscriptions),
        ('AZResourceGroup', resource_groups),
        ('AZVM', vms),
        ('AZKeyVault', key_vaults),
        ('AZAutomationAccount', automation_accounts),
        ('AZContainerRegistry', container_registries),
        ('AZFunctionApp', function_apps),
        ('AZLogicApp', logic_apps),
        ('AZManagedCluster', managed_clusters),
        ('AZVMScaleSet', vm_scale_sets),
        ('AZWebApp', web_apps)
    ) AS counts(node_kind, object_count)
) AS migrated_counts
WHERE object_count > 0
ON CONFLICT (run_id, source_kind, node_kind) DO NOTHING;

